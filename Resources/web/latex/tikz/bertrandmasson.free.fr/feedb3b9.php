	<title>Eh non Mlle Michu les beaux documents ne sont pas réservés uniquement aux geeks !</title> 
	<link href="http://bertrandmasson.free.fr/index.php?article30/eh-non-mlle-michu-les-beaux-documents-ne-sont-pas-reserves-uniquement-aux-geeks"/>
	<id>urn:md5:fbaaca42476dbe34232a00e56a11dac4</id>
	<updated>2011-01-26T21:48:00+01:00</updated>
	<author><name>Bertrand Masson</name></author>
	<dc:subject>LaTeX, planet-edu</dc:subject>
	<content type="html">&lt;p&gt;Toi aussi tu peux utiliser LaTeX. Ce n&#039;est pas très compliqué.&lt;/p&gt;

&lt;p&gt;Le but du document joint &quot;&lt;strong&gt;Le premier rapport de Mlle Michu, étudiante en sciences humaines, réalisé tout en LaTeX&lt;/strong&gt;, avec des illustrations, des tableaux, une bibliographie, une table des matières, une table des ﬁgures et tout ça les doigts dans le nez&quot; est de démystifier LaTeX et de t&#039;aider à réaliser un rapport pas à pas.&lt;/p&gt;

&lt;p&gt;On va essayer de le faire avec le moins de réglages possibles, le plus « brut de décoﬀrage », pour ne pas te noyer dans les arcanes de la programmation LaTeXiènne.&lt;/p&gt;

&lt;p&gt;Le document ﬁnal sera un ﬁchier PDF au format a4, en recto-verso et une police de taille 10 pt, prêt pour l’impression et lisible sur tout type d’ordinateur (Mac, Linux, Windows. . .) avec une très grande qualité typographique.&lt;/p&gt;

&lt;p&gt;Pour la qualité du fond je ne peux pas faire grand chose pour toi, mais après tout c’est ton boulot ! Toutefois comme tu vas t’en rendre compte, LaTeX te déchargeant d’une grande partie du travail de mise en page, tu seras plus libre de te concentrer sur le fond. Avec LaTeX tu gagnes sur les deux tableaux.&lt;/p&gt;

&lt;p&gt;Pour illustrer notre propos, je vais te montrer comment réaliser le rapport de la fouille d’un site préhistorique. Ce choix s’explique, d’une part parce que je suis archéologue préhistorien et inventer un site factice ne m’est pas très difficile et aussi parce qu’un tel rapport contient de nombreux éléments variés, comme des photos, des dessins au trait, des plans pleines pages, des graphiques, des tableaux et donc nous permettra d’aborder de nombreux aspects de la programmation LaTeXiène.&lt;/p&gt;

&lt;p&gt;Tu vas étudier les fonctions LaTeX exactement dans l’ordre ou vont apparaître les difficultés. On aura un récit chronologique et non pas thématique. Ce ne sera pas forcément l’ordre dans lequel tu vas rencontrer les problèmes, lors de la rédaction de ton propre rapport, mais il ne devrait pas y avoir de grosses différences et de toute façon, je pense qu’à la ﬁn de la lecture de ce tutoriel tu
devrais être en mesure de réaliser ton document.&lt;/p&gt;

&lt;p&gt;Dans ces temps d&#039;incertitude quant au devenir des suites bureautiques libres, faire le choix de LaTeX, qui résiste au temps depuis 1985, semble être une idée judicieuse. Même Cyrille Borne y songe &quot;&lt;a href=&quot;http://www.cyrille-borne.com/index.php?post/2010/10/16/Cyrille-BORNE,-blogueur-aigri-et-maintenant-fain%C3%A9ant,-tout-pour-plaire&quot;&gt;si ça commence vraiment à me peser je vais faire comme Christophe et passer à Latex&lt;/a&gt;&quot;.&lt;/p&gt;

&lt;p&gt;Il n&#039;est pas le seul. D&#039;autres qui ne sont pas des profs de math barbu et à sandales s&#039;y mettent. Par exemple, le premier ouvrage d&#039;@rret sur image &lt;a href=&quot;http://www.arretsurimages.net/sarkozistan/&quot;&gt;Crise au Sarkozistan&lt;/a&gt; est réalisé avec LaTeX. &lt;/p&gt;

