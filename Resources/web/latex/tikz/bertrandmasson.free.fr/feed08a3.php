	<content type="html">@David :
Salut David,
Tu as raison j&#039;ai fait une grosse faute dans mon intro à LaTeX  il faut que je corrige.
Pour ta question concernant les dates d&#039;édition et de publication de l&#039;original c&#039;est un peu plus compliqué car il faut trouver le bon style et dans le cas ou tu ne le trouves pas modifier un style existant. Voici un lien qui te donne différent style pour biblatex, : 

http://ctan.tug.org/tex-archive/macros/latex/exptl/biblatex-contrib

tu y trouves un style pour la philo :

http://ctan.tug.org/tex-archive/macros/latex/contrib/biblatex-contrib/biblatex-philosophy

Si tu utilises la TeXLive ce style s&#039;y trouve déjà. Tu l&#039;utilises de cette façon :
\usepackage[style=philosophy-modern]{biblatex}
ou bien
\usepackage[style=philosophy-classic]{biblatex}
ou encore
\usepackage[style=philosophy-verbose]{biblatex}
et si tu compiles avec biber 
\usepackage[backend=biber,style=philosophy-modern]{biblatex}
Je ne sais pas s&#039;il correspond a tes souhaits.
Dans le cas contraire je te conseille de te rapprocher de Maïeul qui fait une thèse sur les religions et qui a bidouillé un style pour pouvoir citer les ouvrages bibliques. Il a un blog ou il donne quelques trucs et je pense qu&#039;il serra plus amène que moi pour te donner la solution à ton problème, son blog : 
href=&quot;http://geekographie.maieul.net/

Pour l&#039;index multiple je ne connais pas le package multind, j&#039;utilisais une autre méthode pour mes index. Mais je peux quand même t&#039;aider. Pour compiler des index multiples il faut impérativement utiliser une console. Je ne sais pas quel os tu utilises mais je vais t&#039;expliquer comment on fait sous linux debian qui est mon os et de mémoire (en espérant ne pas me tromper) sous windows xp que je n&#039;ai pas utilisé depuis fort lontemps.
La console c&#039;est la fenêtre toute noire que les geek utilise pour taper directement des commandes. Sous Debian tu la trouves dans Application -&gt; Accessoires sous le nom de terminal
Sous windows xp tu la trouves dans le menu démarrer également dans accessoire ou bien dans démarrer, puis sur exécuter et tu tapes cmd

Quand tu es dans la console tu dois te rendre dans le répertoire ou ce trouvent tes fichiers .tex et .bib. Sous linux tu tapes :
cd mes-fichiers-tex
ou si le chemin est un peu plus long quelque chose du style 
cd /home/bertrand/mes-fichiers-tex

sous xp :

cd c:\Documents and Settings\mes-fichiers-tex

et maintenant tu tapes (c&#039;est pareil pour linux et xp) 

pdflatex monfichier.tex

puis

makeindex generals

(fait gaffe des fois tu écris general d&#039;autre fois avec un s generals) puis

makeindex authors

puis à nouveau 

pdflatex monfichier.tex

Tu devrais avoir tes index. Tu peux donner d&#039;autre nom à tes index 
\usepackage{multind}
\makeindex{nominum}
\makeindex{rerum} 

dans le corps du texte tu mets  \index{nominum}{idées} ou \index{rerum}{idées} et tu exécutes :

makeindex nominum
makeindex rerum


