Un emc est un Exemple Minimal Complet, c&#039;est à dire un fichier .tex qui ne contient que le strict nécessaire et qui reproduit l&#039;erreur. Par exemple mon fichier pour tester \parencite est le suivant
:
%%debut fichier tex %%
\documentclass [11pt]{scrartcl} 
\usepackage[utf8]{inputenc}
 \usepackage[T1]{fontenc}
 \usepackage[backend=biber,style=authoryear]{biblatex}
\bibliography{bibTest} 
\begin{document} Lorem ipsum dolor sit amet, \parencite{Lechti1999} consectetuer adipiscing elit. Ut purus elit, vestibulum ut, placerat ac \parencite{bidochon2010}, adipiscing  vitae, felis. \end{document}
%%fin fichier tex %%

Auquel puisque l&#039;on test une biblio j&#039;adjoint un fichier .bib qui contient :

%%début fichier bib%%%
@report{Lechti1999,
author={Raoul Lechti},
title={De l&#039;importance du houblon dans une bonne bière},
type={phdthesis},
institution={Université du Temps Libre},
year=1999}


@article{bidochon2010,
author={Raoul Bidochon},
title={Une introduction à BibLaTex},
journaltitle={Les Fiches à Bébert},
year=2010,
issuetitle={Numéro spécial LaTeX},
issuesubtitle={La bibliographie sous LaTeX},
editor={Bébert Lafiche},
editortype={redactor},
indextitle={BibLaTeX, une introduction à}
}
%%fin fichier bib%%%

Fabriquer un emc permet souvent de comprendre où se trouve l&#039;erreur.

D&#039;ailleur en le fabriquant je me suis rendu compte que contrairement à ce que je dit dans les fiches je compile ma biblio avec biber beaucoup plus moderne que bibtex et biblatex le préfère (quand j&#039;ai commencé la rédaction de la fiche sur biblatex il n&#039;était pas encore totalement opérationnel, d&#039;où son absence de la fiche, qu&#039;il faut que je mette à jour, je crois que j&#039;ai déjà dit ça dans un commentaire et je ne l&#039;ai toujours pas fait).
Il faut pour cela mettre [backend=biber]. 
