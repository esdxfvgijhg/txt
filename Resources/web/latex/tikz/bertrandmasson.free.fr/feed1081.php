	<content type="html">&lt;h2&gt;Nouvelle fiche sur : &lt;a href=&quot; http://www.lesfichesabebert.fr/index.php/FichesBebert/Context&quot;&gt; http://www.lesfichesabebert.fr/index.php/FichesBebert/Context&lt;/a&gt;&lt;/h2&gt;Première fiche ConTeXt

L&#039;esprit des fiches de Bébert sur ConTeXt est différent de celles concernant LaTeX. J&#039;ai décidé,  pour des raisons que j&#039;explique dans la fiche, de me mettre à ConTeXt. Comme à chaque foi que je me lance dans de nouveaux apprentissages je prend des notes, beaucoup de notes. Et dans le cas de ConTeXt encore plus car la documentation en français est quasi inexistante et mon anglais indigent. Je me suis rendu compte que la réalisation des fiches sur LaTeX m&#039;avait permis de mieux clarifier et structurer mes connaissances de LaTeX et finalement d&#039;être plus productif. Donc réaliser des fiches sur ConTeXt devrait avoir le même résultat. Enfin pour moi, pour vous je ne suis pas sûr, car la qualité des fiches risque d&#039;être moins bonne. Je ne maîtrise pas ConTeXt (et peu l&#039;anglais) et donc ces fiches sont un état de ma compréhension de la bête. Il risque d&#039;y avoir des erreurs. Vous voilà prévenu. De plus ces fiches sont loin d&#039;être exhaustives, car pour l&#039;instant je survol, plus que j&#039;approfondis, pour me faire une idée et voir si ConTeXt va répondre aux problèmes de mise en page que j&#039;ai rencontré avec LaTeX.

Ces fiches sont réalisées avec ConTeXt, elles sont donc le reflet de mes connaissances, leur qualité esthétique devrait logiquement s&#039;améliorer avec le temps. Elles me serviront aussi de laboratoire et donc devraient passer par des phases plus ou moins farfelues. Pour cette première fiche de présentation de ConTeXt, je ne suis pas allé très loin dans la mise en page, je me suis contenté de celle par défaut.

N&#039;hésitez pas, si vous rencontrez des erreurs, des incompréhensions, d&#039;autres manières de faire plus subtiles de m&#039;en faire part. Cela aura le double objectif de me faire progresser (et je le vaux bien) et d&#039;améliorer la qualité de ces fiches. On obtiendra un début de documentation en français sur ConTeXt (et vous le valez bien).

N&#039;hésitez pas, non plus a me faire des remarques sur l&#039;orthographe, car si mon anglais est indigent, mon orthographe française est pitoyable.

