	<updated>2011-09-01T22:58:00+01:00</updated>
	<author><name>Bertrand Masson</name></author>
	<dc:subject>Non class&amp;eacute;</dc:subject>
	<content type="html">&lt;h2&gt;Nouveau site : &lt;a href=&quot; http://lesfichesabebert.fr/&quot;&gt; http://lesfichesabebert.fr&lt;/a&gt;&lt;/h2&gt;&lt;p&gt;Comme tu as pu le constater il n&#039;y a plus d&#039;activité sur ce site depuis plusieurs mois. La raison est que les progrès effectués ces dernières années par les traitements de texte (Word, Open Office, Libre Office,....) est tel, qu&#039;ils ont largement supplantés LaTeX et qu&#039;il faut être un barbu en short et sandales pour encore utiliser au XXIeme siècle un tel dinosaure.&lt;/p&gt;

&lt;p&gt;Non je déconne, j&#039;utilise plus que jamais LaTeX (enfin plutot ConTeXt, mais c&#039;est une autre histoire). Mais il est vrais qu&#039;il n&#039;y a plus de nouvelles fiches. Et bien c&#039;est parce que j&#039;ai décidé de passer les fiches en version 2.0. Il est comme ça Bébert, toujours à la pointe du progrès.&lt;/p&gt;

&lt;p&gt;Dit Mr Bébert, pourquoi vouloir tout chamboulé les fiches ?&lt;/p&gt;

&lt;p&gt;Il y a au moins deux raisons. &lt;/p&gt;

&lt;p&gt;Premièrement tu as du t&#039;en apercevoir mon orthographe est pitoyable et de nombreux lecteurs m&#039;envoie des commentaires correctifs. Mais voila le caractère figé des fiches rend cette opération laborieuse et pour le lecteur et pour moi. Il est plus facile que celui qui repère une faute, la corrige lui-même.&lt;/p&gt;

&lt;p&gt;Deuxièmement certain des lecteurs des fiches me suggéraient : &lt;/p&gt;

&lt;p&gt;-- Dis Mr Bébert pourquoi tu ne parles pas ce la fonction \truc ? &lt;/p&gt;

&lt;p&gt;ou bien &lt;/p&gt;

&lt;p&gt;-- Et bien moi Mr Bébert je fait pas comme ça mais à la place j&#039;utilise \bidule.&lt;/p&gt;

&lt;p&gt;Et bien oui après plus de 15 ans de LaTeX je ne connais pas tout les \truc et \bidule LaTeXiens, que la forme de mes rapports professionnels évolue peu, donc je suis un peu figé dans mes habitudes. Je ne vois aucun inconvénient à ce que d&#039;autre personne puissent apporter des précisions ou de nouvelles façon de faire, tant que l&#039;on reste dans l&#039;esprit des fiches, à savoir qu&#039;elles doivent toujours être destinées à des débutants.&lt;/p&gt;

&lt;p&gt;Après plusieurs mois de réflexions et plusieurs essais, le wiki m&#039;est apparu comme la solution la plus adaptée aux problèmes décrit ci-dessus.&lt;/p&gt;

&lt;p&gt;Donc j&#039;abandonne le format blog pour transformer le site en wiki. Et c&#039;est pas une mince affaire.&lt;/p&gt;

&lt;p&gt;La contre partie de cette version 2.0 des fiches, censée être plus conviviale, est qu&#039;il faut ré-écrire toutes les fiches et ça c&#039;est un put... de boulot. Donc je vais distiller les fiches ré-écrites au fur et à mesure, dans un nouveau site dont l&#039;adresse est :&lt;/p&gt;

&lt;a href=&quot; http://typo.lesfichesabebert.fr/&quot;&gt; http://typo.lesfichesabebert.fr&lt;/a&gt;&lt;/h2&gt;

&lt;p&gt;Donc ce site restera en vie tant que toutes les fiches ne seront pas basculées en mode wiki, mais il n&#039;y aura plus de nouvelle fiche.&lt;/p&gt;</content>
